/**
 * Represents the log levels for logging messages.
 */
export enum LogLevel {
  /**
   * The debug log level.
   * Use this level for detailed debugging information.
   */
  Debug = 'DEBUG',

  /**
   * The info log level.
   * Use this level for general information messages.
   */
  Info = 'INFO',

  /**
   * The warn log level.
   * Use this level for warning messages.
   */
  Warn = 'WARN',

  /**
   * The error log level.
   * Use this level for error messages.
   */
  Error = 'ERROR',
}
